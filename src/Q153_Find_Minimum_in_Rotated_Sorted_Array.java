	public int findMin2(int[] nums) {
        int left = 0, right = nums.length - 1;

        while (left < right - 1) {
            int mid = (left + right) / 2;
            if (nums[mid] > nums[right])
                left = mid;
            else
                right = mid;
            System.out.println("left = " + nums[left] + ", right = " + nums[right]);
        }

        return Math.min(nums[left], nums[right]);
    }
	
